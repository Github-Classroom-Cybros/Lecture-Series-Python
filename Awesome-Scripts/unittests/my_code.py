"""
MyBasicMath
This class contains basic math operations
"""


class MyBasicMath:
    def mul(self, a, b):
        """
        Multiplication
        :param a:
        :param b:
        :return: a*b
        """
        return a * b

    def sum(self, a, b):
        """
        Sum
        :param a:
        :param b:
        :return: a+b
        """
        return a + b

    def sub(self, a, b):
        """
        Subtraction
        :param a:
        :param b:
        :return:a-b
        """
        return a - b

    def div(self, a, b):
        """
        Division
        :param a:
        :param b:
        :return:a/b
        """
        return a / b
